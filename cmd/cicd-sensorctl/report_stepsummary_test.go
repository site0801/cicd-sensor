func TestRunReportStepSummary_StdinHappyPath(t *testing.T) {
	t.Parallel()

	sample := sampleSummaryLog()
	sample.Hits[0].RuleID = "]((https://evil.example))"
	sample.Hits[0].RuleCondition = "![evil](https://evil.example)"
	sample.Hits[0].Process = &resultdoc.ProcessSummary{ExecPath: "<script>alert(1)"}
	sample.DomainObservations[0].Domain = "evil.example\n# forged"
	sample.DomainObservations[0].Processes = []resultdoc.ObservationProcess{{ExecPath: "<script>domain()"}}
	sample.NetworkConnections[0].Processes = []resultdoc.ObservationProcess{{ExecPath: "/usr/bin/curl"}}
	body, err := json.Marshal(sample)
	if err != nil {
		t.Fatalf("marshal sample: %v", err)
	}

	var stdout, stderr bytes.Buffer
	code, err := runReportStepSummary(context.Background(), []string{
		"--html-url", "https://github.com/acme/project/actions/runs/1/artifacts/2",
	}, bytes.NewReader(body), &stdout, &stderr)
	if err != nil {
		t.Fatalf("runReportStepSummary: %v", err)
	}
	if code != 0 {
		t.Fatalf("exit code: got %d, want 0", code)
	}

	got := stdout.String()
	for _, want := range []string{
		stepSummaryHeaderDetected,
		`width="650"`,
		`### Top 5 rule matches`,
		`<code>]((https://evil.example))</code>`,
		`<code>evil.example # forged</code>`,
		`<code>8.8.8.8:443</code> <code>TCP</code>`,
		`<code>&lt;script&gt;alert(1)</code>`,
		`<code>&lt;script&gt;domain()</code>`,
		`<a href="https://github.com/acme/project/actions/runs/1/artifacts/2#hits">Details</a>`,
		`Generated by`,
	} {
		if !strings.Contains(got, want) {
			t.Fatalf("missing %q in output:\n%s", want, got)
		}
	}
	for _, unwanted := range []string{
		"rule_condition",
		"![evil]",
		"\n# forged",
		`](https://evil.example)`,
		"<script>",
	} {
		if strings.Contains(got, unwanted) {
			t.Fatalf("summary rendered untrusted result detail %q:\n%s", unwanted, got)
		}
	}
}

